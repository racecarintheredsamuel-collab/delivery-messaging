/**
 * Get list of configured custom icons for dropdown options
 * Only returns icons that have either SVG or URL content
 * @param {Object} globalSettings - Global settings containing custom_icons array
 * @returns {Array<{value: string, label: string, svg?: string, url?: string}>}
 */
export function getConfiguredCustomIcons(globalSettings) {
  const customIcons = globalSettings?.custom_icons || [];
  return customIcons
    .map((icon, index) => ({
      value: `custom-${index + 1}`,
      label: icon?.name || `Custom ${index + 1}`,
      svg: icon?.svg || "",
      url: icon?.url || "",
    }))
    .filter(icon => icon.svg || icon.url); // Only return configured icons
}

/**
 * Generate icons object for metafield storage
 * Creates a flat object with all preset icons in both solid and outline styles
 * @returns {Object} - Object with keys like "truck-solid", "truck-outline" and SVG values
 */
export function generateIconsMetafield() {
  const icons = {};
  // Add preset icons (solid and outline)
  for (const { value } of PRESET_ICONS) {
    const solidSvg = getIconSvg(value, "solid");
    const outlineSvg = getIconSvg(value, "outline");
    if (solidSvg) icons[`${value}-solid`] = solidSvg;
    if (outlineSvg) icons[`${value}-outline`] = outlineSvg;
  }
  // Add connector icons (solid only)
  for (const { value } of CONNECTOR_ICONS) {
    const solidSvg = getIconSvg(value, "solid");
    if (solidSvg) icons[`${value}-solid`] = solidSvg;
  }
  return icons;
}
